// Get translation with fallback
function getTranslation(lang, key) {
  return translations[lang]?.[key] ||
         translations['en']?.[key] ||
         translations['cs']?.[key] ||
         key;
}

// Get language flag
function getLanguageFlag(lang) {
  return languageFlags[lang] || '🌍';
}

